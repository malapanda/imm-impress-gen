package imm.impress.beans;


public class Coordinates  {

	private Integer x;
	private Integer y;
	private Integer z;

	public Coordinates(Integer x, Integer y, Integer z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public Integer getX() {
		return x;
	}

	public Integer getY() {
		return y;
	}

	public Integer getZ() {
		return z;
	}

}
